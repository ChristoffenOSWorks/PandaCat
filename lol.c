#include "/usr/include/cairo/cairo.h"
int
main (int argc, char *argv[])
{
        cairo_surface_t *surface = 
            cairo_image_surface_create (CAIRO_FORMAT_ARGB32, 420, 420);
        cairo_t *cr = 
            cairo_create (surface);

        cairo_set_source_rgba(cr, 0.48, 0.48, 0.48, 0.8);
        cairo_scale (cr, 1.0, 1.0);
        cairo_line_to(cr, 11.0, 11.0);
        cairo_line_to(cr, 11.0, 11.0);
        cairo_line_to(cr, 11.0, 1.0);
        cairo_line_to(cr, 1.0, 1.0);
        cairo_line_to(cr, 1.0, 1.0);
        cairo_line_to(cr, 1.0, 1.0);
        cairo_line_to(cr, 1.0, 1.0);
        cairo_line_to(cr, 1.0, 1.0);
        cairo_line_to(cr, 1.0, 1.0);
        cairo_line_to(cr, 1.0, 1.0);
        cairo_line_to(cr, 1.0, 1.0);
        cairo_fill_preserve (cr);
        cairo_set_source_rgba(cr, 0.87, 0.87, 0.87, 0.8);
        cairo_stroke (cr);

        cairo_surface_write_to_png (surface,"deeznutz");
        cairo_destroy (cr);
        cairo_surface_destroy (surface);
        return 0;
}
